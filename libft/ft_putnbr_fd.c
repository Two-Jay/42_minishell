#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*dec;

	dec = "0123456789";
	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	while (n >= 10)
	{
		write(fd, &dec[n % 10], 1);
		n /= 10;
	}
	write(fd, &dec[n % 10], 1);
}
